class FlowEx23 {
    public static void main(String[] args){
        int i = 5;
        System.out.println("while(i--!=0)");
        while(i--!=0) System.out.println(i);
        System.out.println("-------------");
        System.out.println("while(--i!=0)");
        i =5;
        while(--i!=0) System.out.println(i);
    }
}